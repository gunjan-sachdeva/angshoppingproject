 module.exports.createUserDBService = (userDetails) => {


    return new Promise(function myFn(resolve, reject) {
 
        var userModelData = new userModel();
 
        userModelData.id = userDetails.id;
        userModelData.image = userDetails.image;
        userModelData.title = userDetails.title;
        userModelData.description = userDetails.description;
        userModelData.price = userDetails.price;
        userModelData.total = userDetails.total;
        userModelData.rating = userDetails.rating;
        userModelData.brand = userDetails.brand;
        userModelData.instock = userDetails.instock;
        userModelData.quantity = userDetails.quantity;

        userModelData.save(function resultHandle(error, result) {
 
            if (error) {
                reject(false);
            } else {
                resolve(true);
            }
        });
 
    });
 
 }


